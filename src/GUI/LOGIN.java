    private void btnLogin32ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogin32ActionPerformed
        StringBuilder sb = new StringBuilder();
        if (jtf_user32.getText().equals("") || jtf_password32.getText().equals("")) {
            sb.append("Mời bạn nhập đầy đủ thông tin");
            JOptionPane.showMessageDialog(this, sb);
        } else {
            try {
                String sql = "Select * from account where taikhoan=? AND matkhau=?";
                Connection con = Connect.getConnection();
                PreparedStatement p = con.prepareStatement(sql);
                p.setString(1, jtf_user32.getText());
                p.setString(2, jtf_password32.getText());
                ResultSet rs = p.executeQuery();
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Đăng Nhập Thành Công");
                    if(jcb_miss32.isSelected()){
                    JOptionPane.showMessageDialog(this,"Lưu mật khẩu");
                    }
                    Menu lg = new Menu(jtf_user32.getText());
                    lg.setVisible(true);
                    dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Sai Username Hoặc Password");
                }
//                con.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnLogin32ActionPerformed

