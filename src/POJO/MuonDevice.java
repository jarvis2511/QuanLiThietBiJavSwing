public class MuonDevice {
    private String mamuon32;
    private String mathietbi32;
    private String manguoimuon32;
    private String tennguoimuon32;
    private String ngaymuon32;
    private String ngaytra32;

    public MuonDevice() {
    }

    public MuonDevice(String mamuon32, String mathietbi32, String manguoimuon32, String tennguoimuon32, String ngaymuon32, String ngaytra32) {
        this.mamuon32 = mamuon32;
        this.mathietbi32 = mathietbi32;
        this.manguoimuon32 = manguoimuon32;
        this.tennguoimuon32 = tennguoimuon32;
        this.ngaymuon32 = ngaymuon32;
        this.ngaytra32 = ngaytra32;
    }

    public String getMamuon32() {
        return mamuon32;
    }

    public void setMamuon32(String mamuon32) {
        this.mamuon32 = mamuon32;
    }

    public String getMathietbi32() {
        return mathietbi32;
    }

    public void setMathietbi32(String mathietbi32) {
        this.mathietbi32 = mathietbi32;
    }

    public String getManguoimuon32() {
        return manguoimuon32;
    }

    public void setManguoimuon32(String manguoimuon32) {
        this.manguoimuon32 = manguoimuon32;
    }

    public String getTennguoimuon32() {
        return tennguoimuon32;
    }

    public void setTennguoimuon32(String tennguoimuon32) {
        this.tennguoimuon32 = tennguoimuon32;
    }

    public String getNgaymuon32() {
        return ngaymuon32;
    }

    public void setNgaymuon32(String ngaymuon32) {
        this.ngaymuon32 = ngaymuon32;
    }

    public String getNgaytra32() {
        return ngaytra32;
    }

    public void setNgaytra32(String ngaytra32) {
        this.ngaytra32 = ngaytra32;
    }

   
    
}
